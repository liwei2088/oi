/**
 * 第二题：猴子分香蕉
 * 只猴子是好朋友，在海边的椰子树上睡着了。这期间，有商船把一大堆香蕉忘记在沙滩上离去。
 * 第1只猴子醒来，把香蕉均分成5堆，还剩下1个，就吃掉并把自己的一份藏起来继续睡觉。
 * 第2只猴子醒来，重新把香蕉均分成5堆，还剩下2个，就吃掉并把自己的一份藏起来继续睡觉。
 * 第3只猴子醒来，重新把香蕉均分成5堆，还剩下3个，就吃掉并把自己的一份藏起来继续睡觉。
 * 第4只猴子醒来，重新把香蕉均分成5堆，还剩下4个，就吃掉并把自己的一份藏起来继续睡觉。
 * 第5只猴子醒来，重新把香蕉均分成5堆，哈哈，正好不剩！
 * 请计算一开始最少有多少个香蕉。
 * 需要提交的是一个整数，不要填写任何多余的内容。
 */

#include <cstdio>
#include <iostream>
using namespace std;

int main() {
    int x = 6;
    while (1) {
        int s = x;
        if (s / 5 >= 1 && s % 5 == 1) {
            s -= s / 5 + 1;
            if (s / 5 >= 1 && s % 5 == 2) {
                s -= s / 5 + 2;
                if (s / 5 >= 1 && s % 5 == 3) {
                    s -= s / 5 + 3;
                    if (s / 5 >= 1 && s % 5 == 4) {
                        s -= s / 5 + 4;
                        if (s / 5 >= 1 && s % 5 == 0) {
                            cout << x;
                            return 0;
                        }
                    }
                }
            }
        }
        x++;
    }
    return 0;
}